/**
 * Get mock blog tags with pagination support
 * @param {Object} options - Filter options
 * @param {string} options.search - Search term
 * @param {number} options.page - Page number (default: 1)
 * @param {number} options.limit - Items per page (default: 10)
 * @returns {Object} Object with tags array and pagination info
 */
export function getMockBlogTags(options = {}) {
    let tags = [...mockBlogTags];
    const page = options.page || 1;
    const limit = options.limit || 10;

    // Apply search filter
    if (options.search && options.search.trim() !== "") {
        const searchTerm = options.search.toLowerCase().trim();
        tags = tags.filter(
            (tag) =>
                tag.name.toLowerCase().includes(searchTerm) ||
                tag.slug.toLowerCase().includes(searchTerm) ||
                (tag.description &&
                    tag.description.toLowerCase().includes(searchTerm))
        );
    }

    // Calculate pagination
    const total = tags.length;
    const totalPages = Math.ceil(total / limit);
    const startIndex = (page - 1) * limit;
    const endIndex = startIndex + limit;
    const paginatedTags = tags.slice(startIndex, endIndex);

    return {
        tags: paginatedTags,
        pagination: {
            page,
            limit,
            total,
            totalPages,
        },
    };
}
